package aug29th;

public class DataTypesDemo {

	public static void main(String[] args) {
		
		byte a=127;
		
		short b= 32767;
		
		int c=2147483647;
		
		long d=2147483647L;
		
		float e=3.14f;
		double f=3.12457893;
				
		
		System.out.println(a);
		
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		
	
	}

}
